        String email=jTextField7.getText();
        char ch[]=jPasswordField2.getPassword();
        String password=new String(ch);
        lenderId=Integer.parseInt(jTextField9.getText());
        SessionFactory sf=new Configuration().configure().buildSessionFactory();
        Session session=sf.openSession();
        Transaction tx=session.beginTransaction();
        Criteria ct=session.createCriteria(Lender.class);
        ct.add(Restrictions.and(Restrictions.eq("email",email),Restrictions.eq("lenderId", lenderId),Restrictions.eq("password", password)));
        List<Lender> list=ct.list();
        if(list.isEmpty()){
            JOptionPane.showMessageDialog(this,"invalid id/password");
        }
        else{
            new LenderWindow().setVisible(true);
            dispose();
        }
